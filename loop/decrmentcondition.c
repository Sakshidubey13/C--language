#include <stdio.h>

void main()
{
    int n;

    // wap to print N to 10 using while loop.

    printf("Enter the starting number (N): "); // Input from user
    scanf("%d", &n);                           // user input

    // Decrement condition.
    while (n >= 1)// loop will be end when become greter than n.
    {
        printf("%d\n", n);//output the result
        N--;
    }
}
