#include<stdio.h>

void main()
 {
    int a = 500;//variable 500 is assign integer variable x.
    int b = 100;//variable 100 is assign integer variable y.

    printf("Addition = %d\n", a + b);

    // Subtraction
    printf("Subtraction = %d\n", a - b);

    // Multiplication
    printf("Multiplication = %d\n", a * b);

    // Division
    printf("Division = %d\n", a / b);

    // Modulus
    printf("Modulus = %d\n", a % b);
}